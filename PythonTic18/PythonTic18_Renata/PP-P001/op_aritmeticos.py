# op_aritmeticos.py

# Exemplos de operadores aritméticos e compostos
a = 10
b = 3

soma = a + b
subtracao = a - b
multiplicacao = a * b
divisao = a / b
resto = a % b
quociente_inteiro = a // b
potencia = a ** b

# Exemplos de operadores aritméticos compostos
a += 5  # Equivalente a: a = a + 5
b *= 2  # Equivalente a: b = b * 2

# Imprimir resultados
print("Soma:", soma)
print("Subtração:", subtracao)
print("Multiplicação:", multiplicacao)
print("Divisão:", divisao)
print("Resto:", resto)
print("Quociente Inteiro:", quociente_inteiro)
print("Potência:", potencia)
print("Novo valor de a:", a)
print("Novo valor de b:", b)
