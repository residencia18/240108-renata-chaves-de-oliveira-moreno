package TransporteEmpresa;

public class RegistroEmbarque {
	    private Passageiros passageiro;
	    private PontodeParada pontoDeEmbarque;
	    private String cartao;
		public Passageiros getPassageiro() {
			return passageiro;
		}
		public void setPassageiro(Passageiros passageiro) {
			this.passageiro = passageiro;
		}
		public PontodeParada getPontoDeEmbarque() {
			return pontoDeEmbarque;
		}
		public void setPontoDeEmbarque(PontodeParada pontoDeEmbarque) {
			this.pontoDeEmbarque = pontoDeEmbarque;
		}
		public String getCartao() {
			return cartao;
		}
		public void setCartao(String cartao) {
			this.cartao = cartao;
		}

	 
}
